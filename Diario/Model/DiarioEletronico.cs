        {
            get { return id_Disciplina; }
            set { id_Disciplina = value; }
        }

        public int Id_Professor
        {
            get { return id_Professor; }
            set { id_Professor = value; }
        }

        public int Id_Aluno
        {
            get { return id_Aluno; }
            set { id_Aluno = value; }
        }


        public int Id_Ocorrencia
        {
            get { return id_Ocorrencia; }
            set { id_Ocorrencia = value; }
        }


        public int Id_Frequencia
        {
            get { return id_Frequencia; }
            set { id_Frequencia = value; }
        }


        public int Id_PlanoDeAula
        {
            get { return id_PlanoDeAula; }
            set { id_PlanoDeAula = value; }
        }

        public int Id_Turma
        {
            get { return id_Turma; }
            set { id_Turma = value; }
        }

        public int Id_Nota
        {
            get { return id_Nota; }
            set { id_Nota = value; }
        }

    }
}
