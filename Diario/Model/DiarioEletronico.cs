namespace Model
{
    public class DiarioEletronico
    {
        private int id;
        private int id_Disciplina;
        private int id_Professor;
        private int id_Aluno;
        private int id_Ocorrencia;
        private int id_Frequencia;
        private int id_PlanoDeAula;
        private int id_Turma;
        private int id_Nota;

